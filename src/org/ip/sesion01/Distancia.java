package org.ip.sesion01;

public class Distancia {

	public static void main(String[] args) {

		int numero1 = 2;
		int numero2 = 1;
		double resultado = Math.sqrt(numero2 ^ 2 + numero1 ^ 2);

		System.out.println("La distancia del punto (" + numero1 + "," + numero2 + ") al punto (0,0) es " + resultado);

	}
}
